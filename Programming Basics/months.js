function months(num) {
    let countOfMonth = Number(num);
    if (countOfMonth === 1) {
        console.log("January");
    }
    else if (countOfMonth === 2) {
        console.log("February");
    }
    else if (countOfMonth === 3) {
        console.log("March");
    }
    else if (countOfMonth === 4) {
        console.log("April");
    }
    else if (countOfMonth === 5) {
        console.log("May");
    }
    else if (countOfMonth === 6) {
        console.log("June");
    }
    else if (countOfMonth === 7) {
        console.log("July");
    }
    else if (countOfMonth === 8) {
        console.log("August");
    }
    else if (countOfMonth === 9) {
        console.log("September");
    }
    else if (countOfMonth === 10) {
        console.log("October");
    }
    else if (countOfMonth === 11) {
        console.log("November");
    } 
    else if (countOfMonth === 12) {
        console.log("December");
    }
else {
    console.log ("Error!")
}
}
